#include <iostream>
using namespace std;
int N, M;
int a[501][501];

int m[19][3][2]{
    {{0,1}, {0,2}, {0,3}},
    {{1,0}, {2,0}, {3,0}},
    {{1,0}, {1,1}, {1,2}},
    {{0,1}, {1,0}, {2,0}},
    {{0,1}, {0,2}, {1,2}},
    {{1,0}, {2,0}, {2,-1}},
    {{0,1}, {0,2}, {-1,2}},
    {{1,0}, {2,0}, {2,1}},
    {{0,1}, {0,2}, {1,0}},
    {{0,1}, {1,1}, {2,1}},
    {{0,1}, {1,0}, {1,1}},
    {{0,1}, {-1,1}, {-1,2}},
    {{1,0}, {1,1}, {2,1}},
    {{0,1}, {1,1}, {1,2}},
    {{1,0}, {1,-1}, {2,-1}},
    {{0,1}, {0,2}, {-1,1}},
    {{0,1}, {0,2}, {1,1}},
    {{1,0}, {2,0}, {1,1}},
    {{1,0}, {2,0}, {1,-1}}
};

int main(){
    cin >> N >> M;    
    for(int i=0; i<N; i++){
        for(int j=0; j<M; j++){
            cin >> a[i][j];
        }
    }
    int max = -1;
    int sum = 0;
    for(int i=0; i<N; i++){
        for(int j=0; j<M; j++){
            for(int k=0; k<19; k++){
                sum = a[i][j];
                for(int l=0; l<3; l++){
                    int x = i+m[k][l][0];
                    int y = j+m[k][l][1];
                    if(x<N && y<M && x>=0 && y>=0){
                        sum += a[x][y];
                    }
                    else{
                        break;
                    }
                }
                if(sum > max)
                    max = sum;
            }
        }
    }
    cout << max << endl;
    return 0;
}